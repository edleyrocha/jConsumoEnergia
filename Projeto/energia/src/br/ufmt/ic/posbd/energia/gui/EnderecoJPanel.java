    private void editarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarjButtonActionPerformed
        // TODO add your handling code here:
        cancelarjButtonActionPerformed(evt);
        if(tabelajTable.getSelectedRowCount() == 1){
            int linha = tabelajTable.getSelectedRow();
            Endereco edicao = tableModel.get(linha);
            nomejTextField.setText(edicao.getNome());
            cidadejTextField.setText(edicao.getId_cidade() + "");
            idjTextField.setText(edicao.getId() + "");
            cidadejButton1ActionPerformed(evt);
        }else{
            JOptionPane.showMessageDialog(tabelajTable,
                "Selecione somente 1 linha!");
        }
    }//GEN-LAST:event_editarjButtonActionPerformed

    private void excluirjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirjButtonActionPerformed
        // TODO add your handling code here:
        int[] linhas = tabelajTable.getSelectedRows();
        if(JOptionPane.showConfirmDialog(excluirjButton,
            "Deseja realmente excluir?")
        ==JOptionPane.YES_OPTION){

        for (int i = 0; i < linhas.length; i++) {
            Endereco endereco = tableModel.get(linhas[i]);
            dao.excluir(endereco.getId());
        }
        JOptionPane.showMessageDialog(tabelajTable,
            "Endere√ßo removido com Sucesso!");
        tableModel.atualizar(dao.listar());
        }
    }//GEN-LAST:event_excluirjButtonActionPerformed

    private void cidadejButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cidadejButton1ActionPerformed
        // TODO add your handling code here:
        if(!cidadejTextField.getText().equals("")) {
            int id = Integer.parseInt(cidadejTextField.getText());

            try {
                Cidade cidade = new Cidade();
                cidade.setId(id);

                CidadeDAO dao2 = FabricaDAO.criarCidadeDAO();
                nomecidadejTextField1.setText(dao2.consultar(cidade.getId()).getNome());
                ufjTextField.setText(dao2.consultar(cidade.getId()).getUf());
            } catch (Exception e) {
                cidadejTextField.setText("");
                JOptionPane.showMessageDialog(cidadejButton1, "Cidade n√£o encontrada!");
            }
        }else{
            JOptionPane.showMessageDialog(cidadejButton1, "Cidade n√£o encontrada!");
        }
    }//GEN-LAST:event_cidadejButton1ActionPerformed


