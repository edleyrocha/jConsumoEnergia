    private void salvarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButtonActionPerformed
        // TODO add your handling code here:
        Movimentacao movimentacao = new Movimentacao();

        int idimovel_novo = Integer.parseInt(idimoveljTextField.getText());
        movimentacao.setImovel(idimovel_novo);

        int idambiente_novo = Integer.parseInt(idambientejTextField.getText());
        movimentacao.setAmbiente(idambiente_novo);

        int idaparelho_novo = Integer.parseInt(idaparelhojTextField1.getText());
        movimentacao.setAparelho(idaparelho_novo);

        int mes_novo = Integer.parseInt(mesjTextField.getText());
        movimentacao.setMes(mes_novo);
        
        int ano_novo = Integer.parseInt(anojTextField.getText());
        movimentacao.setAno(ano_novo);

        int quantidade_novo = Integer.parseInt(quantidadejTextField.getText());
        movimentacao.setQuantidade(quantidade_novo);
        
        int horas_mes_novo = Integer.parseInt(consumojTextField.getText());
        movimentacao.setHoras_mes(horas_mes_novo);
        
        float valor_novo = Float.parseFloat(custojTextField.getText());
        movimentacao.setValor(valor_novo);
        
        if(!idjTextField.getText().equals("")){
            int id = Integer.parseInt(idjTextField.getText());
            movimentacao.setId(id);
            dao.alterar(movimentacao);
            JOptionPane.showMessageDialog(salvarjButton,
                "Lançamento atualizado com sucesso!");
        }else{
            dao.inserir(movimentacao);
            JOptionPane.showMessageDialog(salvarjButton,
                "Lançamento inserido com sucesso!");
        }

        tableModel.atualizar(dao.listar());

        cancelarjButtonActionPerformed(evt);
    }//GEN-LAST:event_salvarjButtonActionPerformed

