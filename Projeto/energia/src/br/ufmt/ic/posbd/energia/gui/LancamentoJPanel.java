    private void imoveljButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_imoveljButton2ActionPerformed
        // TODO add your handling code here:
        if(!idimoveljTextField.getText().equals("")) {
           int id_imovel = Integer.parseInt(idimoveljTextField.getText());

           try {
               Imovel imovel = new Imovel();
               imovel.setId(id_imovel);
               ImovelDAO daoImovel = FabricaDAO.criarImovelDAO();
               nomeimoveljTextField.setText(daoImovel.consultar(imovel.getId()).getNomeImovel());
               int local_novo = daoImovel.consultar(imovel.getId()).getLocalidade();
               localjTextField.setText(local_novo + "");
               
               int id_endereco = Integer.parseInt(localjTextField.getText());
               Endereco endereco = new Endereco();
               endereco.setId(id_endereco);
               EnderecoDAO daoEndereco = FabricaDAO.criarEnderecoDAO();
               enderecojTextField.setText(daoEndereco.consultar(endereco.getId()).getNome());
               int cidade_novo = daoEndereco.consultar(endereco.getId()).getId_cidade();

               Cidade cidade = new Cidade();
               cidade.setId(cidade_novo);
               CidadeDAO daoCidade = FabricaDAO.criarCidadeDAO();
               nomecidadejTextField.setText(daoCidade.consultar(cidade.getId()).getNome());
               ufjTextField.setText(daoCidade.consultar(cidade.getId()).getUf());
               
            } catch (Exception e) {
                JOptionPane.showMessageDialog(idimoveljTextField, "Imóvel não encontrado!");
            }
        }else{
           JOptionPane.showMessageDialog(idimoveljTextField, "Imóvel não encontrado!");
        }
        
    }//GEN-LAST:event_imoveljButton2ActionPerformed

    private void calcularjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularjButtonActionPerformed
        // TODO add your handling code here:
        int potencia = Integer.parseInt(potenciajTextField.getText());
        int consumo = Integer.parseInt(consumojTextField.getText());
        int quantidade = Integer.parseInt(quantidadejTextField.getText());
        
        int R = (((potencia * consumo) * quantidade) / 1000);
        double tarifa = 0.50765;
        Double valor = (tarifa * R);
        
        String valorstr = new DecimalFormat("0.00").format(valor);
        valorstr = valorstr.replace(",",".");
        custojTextField.setText(valorstr+"");
    }//GEN-LAST:event_calcularjButtonActionPerformed

